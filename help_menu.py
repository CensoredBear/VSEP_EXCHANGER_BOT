STATUS_ORDER = ["user", "operator", "admin", "superadmin"]

def get_help_commands_for_status(status):
    """–í–µ—Ä–Ω—É—Ç—å —Å–ø–∏—Å–æ–∫ –∫–æ–º–∞–Ω–¥ –¥–ª—è help-–º–µ–Ω—é –∏ –º–µ–Ω—é Telegram –ø–æ —Å—Ç–∞—Ç—É—Å—É"""
    commands = []
    for s in STATUS_ORDER:
        commands += HELP_COMMANDS.get(s, [])
        if s == status:
            break
    return commands

def build_help_text(status):
    """–°—Ñ–æ—Ä–º–∏—Ä–æ–≤–∞—Ç—å help-—Ç–µ–∫—Å—Ç –¥–ª—è –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è –ø–æ —Å—Ç–∞—Ç—É—Å—É"""
    commands = get_help_commands_for_status(status)
    text = f"–î–æ—Å—Ç—É–ø–Ω—ã–µ –∫–æ–º–∞–Ω–¥—ã –¥–ª—è —Å—Ç–∞—Ç—É—Å–∞ {status} (–∏ –Ω–∏–∂–µ):\n"
    for cmd, desc in commands:
        text += f"{cmd} ‚Äî {desc}\n"
    return text

